curso = '  PyTHon   '

print(curso.upper()) #coloca tudo em maiusculo
print(curso.lower()) #coloca tudo em minusculo
print(curso.title()) #coloca titulo na primeira letra do texto
print(curso.strip()) #remove espaço em branco do texto no inicio e no final
print(curso.lstrip()) #remove espaço em branco do começ 
print(curso.rstrip()) #remove espalo em branco do final
print(curso.center(30, '#')) #adiciona a string com quantidade de caracter pedido e caracter escolhido
print('.'.join(curso)) #adiciona separadores na variavel 